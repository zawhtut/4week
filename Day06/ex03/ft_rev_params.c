void	ft_putchar(char c);

int		main(int argc, char const *argv[])
{
	int i;
	int j;

	if (argc > 1)
	{
		j = argc - 1;
		while (j >= 1)
		{
			i = 0;
			while (argv[j][i] != '\0')
			{
				ft_putchar(argv[j][i]);
				i++;
			}
			ft_putchar('\n');
			j--;
		}
	}
	return (0);
}
