#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		stl;
	char	*d;

	stl = 0;
	while (src[stl] != '\0')
	{
		++stl;
	}
	d = malloc((stl + 1) * sizeof(char));
	i = 0;
	while (src[i] != '\0')
	{
		d[i] = src[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
